package com.fastfoot.model.entity;

import java.util.Date;
import java.util.Objects;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

@Entity
public class Jogo {//TODO: melhorar nome

	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "jogoSequence")	
	@SequenceGenerator(name = "jogoSequence", sequenceName = "jogo_seq", allocationSize = 8)
	private Long id;
	
	private Date dataCriacao;
	
	public Jogo() {
	}
	
	public Jogo(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getDataCriacao() {
		return dataCriacao;
	}

	public void setDataCriacao(Date dataCriacao) {
		this.dataCriacao = dataCriacao;
	}

	@Override
	public int hashCode() {
		return Objects.hash(id);
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Jogo other = (Jogo) obj;
		return Objects.equals(id, other.id);
	}

}
